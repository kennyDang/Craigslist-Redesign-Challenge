import UIKit

extension UIColor {
    static var customDarkGreen: UIColor {
        return UIColor(red: 22.0/255.0, green: 83.0/255.0, blue: 93.0/255.0, alpha: 1.0)
    }

    static var customRed: UIColor {
        return UIColor(red: 255.0/255.0, green: 106.0/255.0, blue: 103.0/255.0, alpha: 1.0)
    }

    static var customGray: UIColor {
        return UIColor(red: 142.0/255.0, green: 142.0/255.0, blue: 147.0/255.0, alpha: 1.0)
    }
}

